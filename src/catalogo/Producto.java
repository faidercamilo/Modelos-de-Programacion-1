
package catalogo;

public class Producto {
    // Atributos
    public String nombre;
    public String genero;
    public int anio;
    public double rating;
    
    
    // Constructor
    public Producto(String nombre, String genero, int anio, double rating) {
        this.nombre = nombre;
        this.genero = genero;
        this.anio = anio;
        this.rating = rating;
    }
    
}
